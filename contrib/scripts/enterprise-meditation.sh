#!/bin/bash

set -eu
set -o pipefail

UPSTREAM_REPO="cilium\/cilium"
UPSTREAM_BRANCH="${UPSTREAM_BRANCH:-main}"

ARGS=()

# IGNORE_HUNK_REGEXES is an array of flags to 'git diff' that specify string
# matches within a hunk that should be ignored for the purpose of determining
# the mindfulness of the change compared with the merge-base of the upstream
# tree.
IGNORE_HUNK_REGEXES=(
  -I '^.*CODEOWNERS_PATH.*$' # Ignore CODEOWNERS diffs
  -I '^.*configurationFile.*renovate.*$' # Ignore local renovate configuration
  -I $'^(.*[Ee]nterprise.*)|(\`\n\\\')$' # Ignore direct references to enterprise
  -I $'^(.*[Ee]nterprise.*)|(\`\n\\\')|(.*dnsproxy.*)$' # Enterprise dnsproxy image builds
  -I $'^(.*[Ee]nterprise.*)|(\`\n\\\')|(.*shell: bash.*)|(.*run: .*)$' # Enterprise environment overrides
  -I '^.*git config.*ISOVALENT.*$' # Ignore enterprise GitHub secret configurations
  -I '^.*main(-ce)?.*$' # Ignore main->main-ce branch replacements
  -I '.*ubuntu-2[24].04-arm64\]$' # https://github.com/isovalent/cilium/issues/6375
  -I '^.*(Version)|(CEE)|(-ce).*$' # Ignore version references for CEE
  -I $'^(.*cee.*)|^(.*enterprise.*)|(\`\n\\\')|^(.*if.*CTX_ACT_).*)|^(.*IS_ERR.*)|^(.*return [^;]*;)$' # Enterprise BPF hooks
)

# DIFF_EXCL_GLOBS is an array of file patterns to (negatively) match to
# determine the diff against the merge-base of the upstream tree.
#
# These globs are expected to diverge from the upstream main branch, so the
# script always excludes these from consideration as part of the mindfulness
# diff.
DIFF_EXCL_GLOBS=(
  ':!*/*generated*.go' # Auto-generated
  ':!.gitattributes' # Merge directives for go.* in main-ce
  ':!.github/actionlint.yaml' # We use hosted runners in Enterprise
  ':!.github/ISSUE_TEMPLATE/*' # OSS updates these with renovate
  ':!.github/actions/*/k8s-versions.yaml' # K8s versions often diverge
  ':!.github/actions/setup-eks-nodegroup/action.yml' # Enterprise infra tagging for security purposes
  ':!.github/actions/setup-aks-cluster/action.yml' # Enterprise infra tagging for security purposes
  ':!.github/pull_request_template.md' # PR templates can differ
  ':!.github/workflows/ariane-scheduled.yaml' # EOL differs in CEE
  ':!.github/workflows/build-images-ci.yaml' # We build extra CEE components
  ':!.gitignore' # More files to ignore in CEE
  ':!.golangci.yaml' # Enterprise-specific linting configuration
  ':!api/v1/*/*.pb.*' # Auto-generated
  ':!api/v1/*/README.md' # Auto-generated
  ':!CODEOWNERS' # Internal code owners are different from OSS
  ':!Documentation/cmdref/*' # Auto-generated
  ':!Documentation/helm-values.rst' # Auto-generated
  ':!Documentation/spelling_wordlist.txt' # Unmaintained in Enterprise
  ':!Documentation/update-cmdref.sh' # Enterprise-specific changes
  ':!go.*' # Go dependencies and autogenerated files, inconsequential
  ':!install/kubernetes/Makefile*' # Enterprise-only chart changes
  ':!install/kubernetes/cilium/Chart.yaml' # Enterprise-only chart changes
  ':!install/kubernetes/cilium/README.md' # Enterprise-only chart changes
  ':!install/kubernetes/cilium/templates/NOTES.txt' # Enterprise-only chart changes
  ':!install/kubernetes/cilium/templates/_extensions.tpl' # Enterprise-only chart changes
  ':!install/kubernetes/cilium/values*' # Enterprise-only chart changes
  ':!pkg/datapath/config/*_config.go' # Code generated by dpgen
  ':!pkg/k8s/client/clientset/versioned/clientset.go' # Auto-generated
  ':!pkg/k8s/client/clientset/versioned/*/register.go' # Auto-generated
  ':!pkg/k8s/client/informers/externalversions/factory.go' # Auto-generated
  ':!pkg/k8s/client/informers/externalversions/generic.go' # Auto-generated
  ':!vendor/' # Enterprise can vendor its own dependencies
)

# DIFF_NOISY_GLOBS is an array in the same form as DIFF_EXCL_GLOBS, and these
# filters are also applied by default. They may however include non-mindful
# changes, so they can be optionally displayed if the user passes
# '-n'|'--noisy' to this script.
DIFF_NOISY_GLOBS=(
  ':!.github/workflows/renovate.yaml' # Renovate needs Quay login for CEE
  ':!.github/actions/set-runtime-image/action.yml' # Enterprise overrides
  ':!api/v1/observer/observer.proto' # Enterprise-only APIs
  ':!contrib/scripts/k8s-code-gen.sh' # Generate Enterprise k8s objects
  ':!pkg/k8s/apis/cell.go' # Register Enterprise CRDs
  ':!pkg/k8s/synced/crd.go' # Register Enterprise CRDs
  ':!test/helpers/kubectl.go' # https://github.com/isovalent/cilium/issues/7112
)

# BPF_NOISY_GLOBS is an array, like the ones defined above, which filters out
# BPF code files where we have accepted a trade-off in mindfulness.
BPF_NOISY_GLOBS=(
  ':!bpf/lib/egress_gateway.h'
  ':!bpf/lib/policy_log.h' # https://github.com/isovalent/cilium/issues/7110
)

# CEC_NOISY_GLOBS is an array, like the ones defined above, which filters out
# CEC code files where we have accepted a trade-off in mindfulness.
CEC_NOISY_GLOBS=(
  ':!pkg/ciliumenvoyconfig/cec_resource_parser.go'
  ':!pkg/ciliumenvoyconfig/cell.go'
  ':!pkg/ciliumenvoyconfig/exp_test.go'
  ':!pkg/ciliumenvoyconfig/legacy/cell.go'
  ':!pkg/ciliumenvoyconfig/script_test.go'
)

usage() {
  echo -e "$0 [FLAGS] [COMMIT]"
  echo
  echo -e 'Print a diff of file changes compared with the upstream tree at the most recent merge-base.'
  echo
  echo -e 'Positional Arguments:'
  echo -e 'COMMIT\tCommit to compare with (default: Compare with merge-base of upstream default branch)'
  echo
  echo -e 'Flags:'
  echo -e '-a | --apply\tApply the diff to the local tree to sync with upstream'
  echo -e '-d | --debug\tRun the script with debug output (-x)'
  echo -e '     --exit-code\tBehave like diff(1) when encountering a diff (ie exit status 1 on diff)'
  echo -e '-f | --fetch\tFetch the latest upstream repo before assessing mindfulness'
  echo -e '-h | --help\tDisplay this help message'
  echo -e '-n | --noisy\tPrint noisy candidates that often diverge from upstream for trivial reasons'
  echo -e '-r | --repo\tSpecify the target repository to sync (example: "cilium/cilium")'
}

# $1 - target commit
diff_upstream() {
  commit="$1"

  git diff "$commit" "HEAD" "${ARGS[@]}" \
    --merge-base --diff-filter=BMUX \
    "${IGNORE_HUNK_REGEXES[@]}" \
    -- "${DIFF_EXCL_GLOBS[@]}" \
       "${DIFF_NOISY_GLOBS[@]}" \
       "${BPF_NOISY_GLOBS[@]}" \
       "${CEC_NOISY_GLOBS[@]}"
}

main() {
  local apply="" upstream_remote="" commit="" debug="" fetch=""

  set +e
  while [[ $# -gt 0 ]]; do
    case $1 in
      -a|--apply)
        apply=1
        ;;
      -d|--debug)
        debug=1
        ;;
      --exit-code)
        ARGS+=('--exit-code')
        ;;
      -f|--fetch)
        fetch=1
        ;;
      -h|--help)
        >&2 usage
        exit 0
        ;;
      -n|--noisy)
        DIFF_NOISY_GLOBS=()
        BPF_NOISY_GLOBS=()
	CEC_NOISY_GLOBS=()
        ;;
      -r|--repo)
        UPSTREAM_REPO="$2"
        shift
          ;;
      -s|--stat)
        ARGS+=('--stat')
        ;;
      -*)
        >&2 echo "unknown option: $1"
        >&2 usage
        exit 1
        ;;
      *)
        commit="$1"
        shift
        if [[ $# -gt 0 ]]; then
            >&2 echo "Unrecognized args: $*"
            >&2 usage
            exit 1
        fi
    esac
    shift
  done
  set -e

  if [[ -n "$debug" ]]; then
      set -x
  fi

  if [[ -z $commit ]]; then
    upstream_remote=$(git remote -v | grep "${UPSTREAM_REPO}.*fetch" | awk '{ print $1; }')
    if [[ -n "$fetch" ]]; then
      git fetch --quiet "$upstream_remote"
    fi
    commit="$upstream_remote/${UPSTREAM_BRANCH}"
  fi

  >&2 echo "Scanning diff HEAD..$commit:"
  >&2 git log -1 --oneline "$commit"

  if [[ -z $apply ]]; then
    diff_upstream "$commit"
  else
    patch=$(mktemp tmp.XXXXXXXX.patch)
    trap 'rm $patch' EXIT
    diff_upstream "$commit" > "$patch"
    git apply --reverse "$patch"
  fi
}

main "$@"
