package diagnostics

import (
	"fmt"
	"math"
	"os"
	"path/filepath"
	"reflect"
	"runtime"
	"strings"
)

func funcNameAndLocation(fn any) string {
	f := runtime.FuncForPC(reflect.ValueOf(fn).Pointer())
	file, line := f.FileLine(f.Entry())
	name := f.Name()
	name = strings.TrimSuffix(name, "-fm")
	// Truncate the path in name as it'll be repeated in file path
	if idx := strings.LastIndex(name, "/"); idx > 0 {
		name = name[idx+1:]
	}
	if file != "<autogenerated>" {
		return fmt.Sprintf("%s (%s:%d)", name, usefulPathSegment(file), line)
	}
	return name
}

func usefulPathSegment(file string) string {
	// Purely a heuristic.
	var commonRoots = map[string]struct{}{
		"pkg": {},
		"cmd": {},
	}
	p := filepath.Clean(file)
	segs := strings.Split(p, string(os.PathSeparator))

	for i := len(segs) - 1; i > 0; i-- {
		if _, ok := commonRoots[segs[i]]; ok {
			segs = segs[i:]
			break
		}
	}

	// Truncate to at most 3 segments
	if len(segs) > 3 {
		segs = segs[len(segs)-4:]
		segs[0] = "..."
	}

	return filepath.Join(segs...)
}

func prettyValue(v float64) string {
	unit, multp := chooseUnit(v)
	return fmt.Sprintf("%.4g%s", v*multp, unit)
}

func chooseUnit(v float64) (string, float64) {
	unit := ""
	multp := 1.0
	v = math.Abs(v)
	switch {
	case v == 0.0:
	case v > 1_000_000_000_000:
		unit = "T"
		multp = 0.000_000_000_001
	case v > 1_000_000_000:
		unit = "G"
		multp = 0.000_000_001
	case v > 1_000_000:
		unit = "M"
		multp = 0.000_001
	case v > 1000:
		unit = "k"
		multp = 0.001
	case v < 0.000_000_001:
		unit = "p"
		multp = 1_000_000_000_000
	case v < 0.000_001:
		unit = "n"
		multp = 1_000_000_000
	case v < 0.001:
		unit = "µ"
		multp = 1_000_000
	case v < 1:
		unit = "m"
		multp = 1000
	}
	return unit, multp
}
