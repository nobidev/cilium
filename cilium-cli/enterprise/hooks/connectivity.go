func (ec *EnterpriseConnectivity) addExternalCiliumDNSProxyTests(ct *check.ConnectivityTest, templates map[string]string) error {
	test := check.NewTest("cilium-dns-proxy-ha", ct.Params().Verbose, ct.Params().Debug)
	ct.AddTest(test).
		WithCiliumPolicy(templates["clientEgressOnlyDNSPolicyYAML"]).
		WithCiliumPolicy(clientEgressDNSToEchoYAML).
		WithFeatureRequirements(features.RequireEnabled(enterpriseFeatures.CiliumDNSProxyHA)).
		WithScenarios(enterpriseTests.ExternalCiliumDNSProxy(ec.externalCiliumDNSProxyPods)).
		WithExpectations(func(a *check.Action) (egress, ingress check.Result) {
			return check.ResultOK.ExpectMetricsIncrease(
					enterpriseTests.ExternalCiliumDNSProxySource(ec.externalCiliumDNSProxyPods),
					"isovalent_external_dns_proxy_policy_l7_total",
				),
				check.ResultNone
		})
	return nil
}

func (ec *EnterpriseConnectivity) addPhantomServiceTests(cts ...*check.ConnectivityTest) (err error) {
	if cts[0].Params().MultiCluster == "" {
		return nil
	}

	if ok, _ := cts[0].Features.MatchRequirements(features.RequireEnabled(enterpriseFeatures.PhantomServices)); !ok {
		return nil
	}

	defer func() {
		if r := recover(); r != nil {
			err = r.(error)
		}
	}()

	mustGetTest("no-policies", cts...).WithSetupFunc(deploy.PhantomService).WithScenarios(enterpriseTests.PodToPhantomService())
	mustGetTest("allow-all-except-world", cts...).WithSetupFunc(deploy.PhantomService).WithScenarios(enterpriseTests.PodToPhantomService())

	// Traffic shall be dropped, because it is subject to the ingress/egress policy.
	mustGetTest("all-ingress-deny", cts...).WithSetupFunc(deploy.PhantomService).WithScenarios(enterpriseTests.PodToPhantomService())
	mustGetTest("all-ingress-deny-knp", cts...).WithSetupFunc(deploy.PhantomService).WithScenarios(enterpriseTests.PodToPhantomService())
	mustGetTest("all-egress-deny", cts...).WithSetupFunc(deploy.PhantomService).WithScenarios(enterpriseTests.PodToPhantomService())
	mustGetTest("all-egress-deny-knp", cts...).WithSetupFunc(deploy.PhantomService).WithScenarios(enterpriseTests.PodToPhantomService())
	mustGetTest("cluster-entity-multi-cluster", cts...).WithSetupFunc(deploy.PhantomService).WithScenarios(enterpriseTests.PodToPhantomService())

	// Traffic shall be allowed, because it matches the cross-cluster policy.
	mustGetTest("client-egress", cts...).WithSetupFunc(deploy.PhantomService).WithScenarios(enterpriseTests.PodToPhantomService())
	mustGetTest("client-egress-knp", cts...).WithSetupFunc(deploy.PhantomService).WithScenarios(enterpriseTests.PodToPhantomService())

	return
}

func (ec *EnterpriseConnectivity) addEgressGatewayHATests(ct *check.ConnectivityTest, templates map[string]string) (err error) {
	newTest := func(ct *check.ConnectivityTest, name string) *enterpriseCheck.EnterpriseTest {
		return enterpriseCheck.NewEnterpriseConnectivityTest(ct).
			NewEnterpriseTest(name).
			WithFeatureRequirements(
				features.RequireEnabled(enterpriseFeatures.EgressGatewayHA),
				features.RequireEnabled(features.NodeWithoutCilium))
	}

	// prefix the test name with `seq-` to run it sequentially
	newTest(ct, "seq-egress-gateway-ha").
		WithIsovalentEgressGatewayPolicy(enterpriseCheck.IsovalentEgressGatewayPolicyParams{
			Name:            "iegp-sample-client",
			PodSelectorKind: "client",
			EgressGroup:     enterpriseCheck.SingleGateway,
		}).
		WithIsovalentEgressGatewayPolicy(enterpriseCheck.IsovalentEgressGatewayPolicyParams{
			Name:            "iegp-sample-echo",
			PodSelectorKind: "echo",
			EgressGroup:     enterpriseCheck.SingleGateway,
		}).
		WithIPRoutesFromOutsideToPodCIDRs().
		WithScenarios(enterpriseTests.EgressGatewayHA())

	if versioncheck.MustCompile(">=1.16.0")(ct.CiliumVersion) {
		// prefix the test name with `seq-` to run it sequentially
		newTest(ct, "seq-egress-gateway-ha-with-l7-policy").
			WithIsovalentEgressGatewayPolicy(enterpriseCheck.IsovalentEgressGatewayPolicyParams{
				Name:            "iegp-sample-client",
				PodSelectorKind: "client",
				EgressGroup:     enterpriseCheck.SingleGateway,
			}).
			WithIsovalentEgressGatewayPolicy(enterpriseCheck.IsovalentEgressGatewayPolicyParams{
				Name:            "iegp-sample-echo",
				PodSelectorKind: "echo",
				EgressGroup:     enterpriseCheck.SingleGateway,
			}).
			WithCiliumPolicy(clientEgressICMPYAML).
			WithCiliumPolicy(templates["clientEgressOnlyDNSPolicyYAML"]). // DNS resolution only
			WithCiliumPolicy(clientEgressL7HTTPAnywhereYAML).             // L7 allow policy with HTTP introspection
			WithIPRoutesFromOutsideToPodCIDRs().
			WithFeatureRequirements(features.RequireEnabled(features.L7Proxy)).
			WithScenarios(enterpriseTests.EgressGatewayHA())
	}

	// prefix the test name with `seq-` to run it sequentially
	newTest(ct, "seq-egress-gateway-ha-excluded-cidrs").
		WithIsovalentEgressGatewayPolicy(enterpriseCheck.IsovalentEgressGatewayPolicyParams{
			Name:            "iegp-sample-client",
			PodSelectorKind: "client",
			EgressGroup:     enterpriseCheck.SingleGateway,
			ExcludedCIDRs:   enterpriseCheck.ExternalNodeExcludedCIDRs,
		}).
		WithIPRoutesFromOutsideToPodCIDRs().
		WithScenarios(enterpriseTests.EgressGatewayExcludedCIDRs())

	// prefix the test name with `seq-` to run it sequentially
	newTest(ct, "seq-egress-gateway-ha-multiple-gateways").
		WithIsovalentEgressGatewayPolicy(enterpriseCheck.IsovalentEgressGatewayPolicyParams{
			Name:            "iegp-sample-client",
			PodSelectorKind: "client",
			EgressGroup:     enterpriseCheck.AllCiliumNodes,
		}).
		WithScenarios(enterpriseTests.EgressGatewayMultipleGateways())

	if versioncheck.MustCompile(">=1.16.0")(ct.CiliumVersion) {
		// prefix the test name with `seq-` to run it sequentially
		newTest(ct, "seq-egress-gateway-ha-multiple-gateways-with-l7-policy").
			WithIsovalentEgressGatewayPolicy(enterpriseCheck.IsovalentEgressGatewayPolicyParams{
				Name:            "iegp-sample-client",
				PodSelectorKind: "client",
				EgressGroup:     enterpriseCheck.AllCiliumNodes,
			}).
			WithCiliumPolicy(clientEgressICMPYAML).
			WithCiliumPolicy(templates["clientEgressOnlyDNSPolicyYAML"]). // DNS resolution only
			WithCiliumPolicy(clientEgressL7HTTPAnywhereYAML).             // L7 allow policy with HTTP introspection
			WithFeatureRequirements(features.RequireEnabled(features.L7Proxy)).
			WithScenarios(enterpriseTests.EgressGatewayMultipleGateways())
	}

	if versioncheck.MustCompile(">=1.14.8")(ct.CiliumVersion) {
		// prefix the test name with `seq-` to run it sequentially
		egwHAAZAffinityTest := newTest(ct, "seq-egress-gateway-ha-az-affinity").
			WithIsovalentEgressGatewayPolicy(enterpriseCheck.IsovalentEgressGatewayPolicyParams{
				Name:            "iegp-sample-client-az-affinity",
				PodSelectorKind: "client",
				EgressGroup:     enterpriseCheck.AllCiliumNodesWithAZAffinity,
				// We update the AZAffinity: "localOnly" setting after topology.kubernetes.io/zone has been set on the
				// Gateway nodes. This prevents error logs about missing node AZs from being emitted.
			})
		egwHAAZAffinityTest.WithScenarios(enterpriseTests.EgressGatewayAZAffinity(egwHAAZAffinityTest.Context().EntClients()))
	}

	newIPAMTest := func(ct *check.ConnectivityTest, name string) *enterpriseCheck.EnterpriseTest {
		et := enterpriseCheck.NewEnterpriseConnectivityTest(ct).
			NewEnterpriseTestWithoutSetup(name).
			WithFeatureRequirements(
				features.RequireEnabled(enterpriseFeatures.EgressGatewayHA),
				features.RequireEnabled(features.NodeWithoutCilium))
		et.WithSetupFunc(func(ctx context.Context, t *check.Test, ct *check.ConnectivityTest) error {
			// get egress CIDRs to be used for IPAM before applying the policies
			egressCIDRs := enterpriseTests.IPAMEgressCIDRs(ctx, t, ct)
			if len(egressCIDRs) == 0 {
				egressCIDRs = defaults.EgressGatewayCIDRsDefault
			}
			t.Logf("Using egress CIDRs %v for IEGP IPAM", egressCIDRs)
			et.WithEgressCIDRsforIEGP("iegp-sample-client", egressCIDRs)
			return et.Setup(ctx)
		})
		return et
	}

	if versioncheck.MustCompile(">=1.16.0")(ct.CiliumVersion) {
		// prefix the test name with `seq-` to run it sequentially
		newIPAMTest(ct, "seq-egress-gateway-ha-ipam").
			WithIsovalentEgressGatewayPolicy(enterpriseCheck.IsovalentEgressGatewayPolicyParams{
				Name:            "iegp-sample-client",
				PodSelectorKind: "client",
				EgressGroup:     enterpriseCheck.SingleGateway,
			}).
			WithIPRoutesFromOutsideToPodCIDRs().
			WithScenarios(enterpriseTests.EgressGatewayHAIPAM())
	}

	if versioncheck.MustCompile(">=1.16.0")(ct.CiliumVersion) {
		// prefix the test name with `seq-` to run it sequentially
		newIPAMTest(ct, "seq-egress-gateway-ha-ipam-multiple-gateways").
			WithIsovalentEgressGatewayPolicy(enterpriseCheck.IsovalentEgressGatewayPolicyParams{
				Name:            "iegp-sample-client",
				PodSelectorKind: "client",
				EgressGroup:     enterpriseCheck.AllCiliumNodes,
			}).
			WithIPRoutesFromOutsideToPodCIDRs().
			WithScenarios(enterpriseTests.EgressGatewayHAIPAMMultipleGateways())
	}

	// This test depends on the Route Reflector feature of the BGP CP, which requires using
	// v1.18.1 or later.
	if versioncheck.MustCompile(">=1.18.1")(ct.CiliumVersion) {
		// prefix the test name with `seq-` to run it sequentially
		newIPAMTest(ct, "seq-egress-gateway-ha-ipam-bgp-advertisement").
			WithFeatureRequirements(
				features.RequireEnabled(enterpriseFeatures.EnterpriseBGPControlPlane),
				features.RequireEnabled(enterpriseFeatures.BFD),
			).
			WithCondition(func() bool { return len(enterpriseTests.Params.EgressGateway.PeerAddresses) != 0 }).
			WithIsovalentEgressGatewayPolicy(enterpriseCheck.IsovalentEgressGatewayPolicyParams{
				Name:            "iegp-sample-client",
				Labels:          map[string]string{"egw": "bgp-advertise"},
				PodSelectorKind: "client",
				EgressGroup:     enterpriseCheck.AllCiliumNodes,
			}).
			WithScenarios(enterpriseTests.EgressGatewayHABGPAdvertisement())
	}

	return nil
}

func (ec *EnterpriseConnectivity) addEncryptionPolicyTests(cts ...*check.ConnectivityTest) (err error) {
	defer func() {
		if r := recover(); r != nil {
			err = r.(error)
		}
	}()

	// Regular encryption related tests cannot be run (neither in sanity nor assert mode) if
	// encryption policies are enabled, as not all traffic in the cluster will be encrypted.
	mustGetTest("pod-to-pod-encryption", cts...).WithFeatureRequirements(
		features.RequireDisabled(enterpriseFeatures.EncryptionPolicy),
		features.RequireDisabled(enterpriseFeatures.RemoteEncryptionPolicy),
	)
	mustGetTest("pod-to-pod-with-l7-policy-encryption", cts...).WithFeatureRequirements(
		features.RequireDisabled(enterpriseFeatures.EncryptionPolicy),
		features.RequireDisabled(enterpriseFeatures.RemoteEncryptionPolicy),
	)
	mustGetTest("node-to-node-encryption", cts...).WithFeatureRequirements(
		features.RequireDisabled(enterpriseFeatures.EncryptionPolicy),
		features.RequireDisabled(enterpriseFeatures.RemoteEncryptionPolicy),
	)
	mustGetTest("pod-to-pod-encryption-v2", cts...).WithFeatureRequirements(
		features.RequireDisabled(enterpriseFeatures.EncryptionPolicy),
		features.RequireDisabled(enterpriseFeatures.RemoteEncryptionPolicy),
	)
	mustGetTest("pod-to-pod-with-l7-policy-encryption-v2", cts...).WithFeatureRequirements(
		features.RequireDisabled(enterpriseFeatures.EncryptionPolicy),
		features.RequireDisabled(enterpriseFeatures.RemoteEncryptionPolicy),
	)

	newTest := func(ct *check.ConnectivityTest, name string) *enterpriseCheck.EnterpriseTest {
		return enterpriseCheck.NewEnterpriseConnectivityTest(ct).
			NewEnterpriseTest(name).
			WithFeatureRequirements(features.RequireEnabled(enterpriseFeatures.EncryptionPolicy)).
			WithCondition(func() bool {
				if ct.Params().MultiCluster != "" {
					if ok, _ := ct.Features.MatchRequirements(features.RequireEnabled(enterpriseFeatures.RemoteEncryptionPolicy)); !ok {
						return false
					}
				}
				return true
			}).
			WithCondition(func() bool { return !ct.Params().SingleNode })
	}

	if versioncheck.MustCompile(">=1.16.0")(cts[0].CiliumVersion) {
		newTest(cts[0], "pod-to-pod-encryption-policy").
			WithIsovalentClusterwideEncryptionPolicy(encryptClientToEchoYAML).
			WithScenarios(enterpriseTests.PodToPodEncryptionPolicy())
	}

	return nil
}

func (ec *EnterpriseConnectivity) addMixedRoutingTests(cts ...*check.ConnectivityTest) (err error) {
	defer func() {
		if r := recover(); r != nil {
			err = r.(error)
		}
	}()

	// Encryption related tests cannot be run (neither in sanity nor assert mode) if
	// the two clusters are configured with different routing modes, because the
	// tcpdump filters to assert no unencrypted packet is leaked would be incorrect.
	mustGetTest("pod-to-pod-encryption", cts...).WithFeatureRequirements(features.RequireDisabled(enterpriseFeatures.MixedRoutingMode))
	mustGetTest("node-to-node-encryption", cts...).WithFeatureRequirements(features.RequireDisabled(enterpriseFeatures.MixedRoutingMode))
	mustGetTest("pod-to-pod-encryption-v2", cts...).WithFeatureRequirements(features.RequireDisabled(enterpriseFeatures.MixedRoutingMode))
	mustGetTest("pod-to-pod-with-l7-policy-encryption-v2", cts...).WithFeatureRequirements(features.RequireDisabled(enterpriseFeatures.MixedRoutingMode))

	// Requirements changes should be additionally applied to the ones guarding
	// the execution of the associated setup function, so that they always match.
	if cts[0].Params().IncludeUnsafeTests {
		// Generate extra traffic from one pod in each cluster to each Cilium
		// HealthIP, to prevent the sanity checks from reporting false positives
		// due to the lack of other traffic flowing in the cluster. The already
		// existing health check test is not sufficient when WireGuard is enabled,
		// because node to pod traffic is not encrypted by default.
		cts[0].AddTest(check.NewTest("mixed-routing-extra-traffic", cts[0].Params().Verbose, cts[0].Params().Debug)).
			WithFeatureRequirements(
				features.RequireEnabled(features.HealthChecking),
				features.RequireEnabled(enterpriseFeatures.FallbackRoutingMode),
			).
			WithScenarios(enterpriseTests.MixedRoutingExtraTraffic())

		cts[0].AddTest(check.NewTest("mixed-routing", cts[0].Params().Verbose, cts[0].Params().Debug)).
			WithFeatureRequirements(features.RequireEnabled(enterpriseFeatures.FallbackRoutingMode)).
			WithSysdumpPolicy(check.SysdumpPolicyNever).
			WithScenarios(ec.mixedRoutingScenario)
	}

	return nil
}

