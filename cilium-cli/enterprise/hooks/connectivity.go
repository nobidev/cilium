func (ec *EnterpriseConnectivity) addConnectivityTests(cts ...*check.ConnectivityTest) error {
	templates, err := renderTemplates(cts[0])
	if err != nil {
		return fmt.Errorf("cannot render templates: %w", err)
	}

	if err := ec.addHubbleVersionTests(cts...); err != nil {
		return err
	}

	if err := ec.addExternalCiliumDNSProxyTests(cts[0], templates); err != nil {
		return err
	}

	if err := ec.addPhantomServiceTests(cts...); err != nil {
		return err
	}

	if cts[0].Params().IncludeUnsafeTests {
		if err := ec.addEgressGatewayHATests(cts[0], templates); err != nil {
			return err
		}
	}

	if err := ec.addMulticastTests(cts[0]); err != nil {
		return err
	}

	if err := ec.addBGPTests(cts[0]); err != nil {
		return err
	}

	if err := ec.addBFDTests(cts[0]); err != nil {
		return err
	}

	if err := ec.addIngressPolicyTests(cts[0], templates); err != nil {
		return err
	}

	if err := ec.addEncryptionPolicyTests(cts...); err != nil {
		return err
	}

	if err := ec.addOrderedPolicyTests(cts[0], templates); err != nil {
		return err
	}

	// Always keep the mixed routing mode tests last, as they assert that the
	// correct routing mode was used to forward the packets generated by all
	// the other tests.
	if err := ec.addMixedRoutingTests(cts...); err != nil {
		return err
	}

	return nil
}

